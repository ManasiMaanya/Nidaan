export const speakText = (text, lang = 'en') => {
  if ('speechSynthesis' in window) {
    window.speechSynthesis.cancel()
    const utterance = new SpeechSynthesisUtterance(text)

    switch (lang) {
      case 'hi': utterance.lang = 'hi-IN'; break
      case 'ta': utterance.lang = 'ta-IN'; break
      default: utterance.lang = 'en-IN'
    }

    // Warm, conversational pacing
    utterance.rate = 0.88
    utterance.pitch = 1.08
    utterance.volume = 1

    // Pick the most natural-sounding voice available
    const voices = window.speechSynthesis.getVoices()
    const preferredVoice = voices.find(v =>
      v.lang === utterance.lang && (
        v.name.toLowerCase().includes('female') ||
        v.name.toLowerCase().includes('natural') ||
        v.name.toLowerCase().includes('neural') ||
        v.name.toLowerCase().includes('enhanced') ||
        v.name.toLowerCase().includes('samantha') ||
        v.name.toLowerCase().includes('google') ||
        v.name.toLowerCase().includes('premium')
      )
    ) || voices.find(v => v.lang === utterance.lang)
      || voices.find(v => v.lang.startsWith(lang))

    if (preferredVoice) utterance.voice = preferredVoice

    window.speechSynthesis.speak(utterance)
  }
}

export const stopSpeaking = () => {
  if ('speechSynthesis' in window) window.speechSynthesis.cancel()
}

