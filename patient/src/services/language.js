const getPageTextEnglish = (pageName, patientData, extraData) => {
  const name = patientData?.name?.split(' ')[0] || 'there'

  switch (pageName) {
    case 'home':
      return `Hello ${name}! Welcome to Nidaan, your personal health companion. ` +
        `Here on the home screen, you have three things you can do. ` +
        `First, you can go to My Records to see all your medical reports. ` +
        `Second, you can Share with Doctor — this creates a QR code you can show your doctor during your visit. ` +
        `And third, you can Share with Family, which lets trusted family members always have access to your health information. ` +
        `Just tap the button you need. I am here to help!`

    case 'records': {
      const recs = extraData?.records || []
      if (recs.length === 0) {
        return `This is your Records page, ${name}. You don't have any reports added yet. ` +
          `Once your doctor adds a report, it will appear here for you to read.`
      }
      const recSummaries = recs.map((r, i) =>
        `Report ${i + 1} is your ${r.title}, done by ${r.doctor} at ${r.hospital} on ${r.date}. ${r.summary}.`
      ).join('  ')
      return `Welcome to your Records page, ${name}. ` +
        `You have ${recs.length} medical report${recs.length > 1 ? 's' : ''} saved here. ` +
        `Just tap on any report to read it in full, along with a simple explanation in plain language. ` +
        `Here is a quick summary of what you have.  ${recSummaries}  ` +
        `Tap any report card to learn more.`
    }

    case 'recordDetail': {
      const r = extraData?.record
      if (!r) return `Let me open that report for you.`
      return `Okay ${name}, let me walk you through this report. ` +
        `This is your ${r.title}. ` +
        `It was done by ${r.doctor}, at ${r.hospital}, on ${r.date}. ` +
        `Here is the summary in simple words. ${r.simplified}. ` +
        `If you want to hear more details, you can scroll down to read the full report. ` +
        `And when you are done, tap the back button to return to your reports.`
    }

    case 'profile':
      if (!patientData) return `This is your profile page.`
      return `This is your profile page, ${name}. ` +
        `It has all your important personal details saved in one place. ` +
        `Your name is ${patientData.name}. ` +
        `You are ${patientData.age} years old. ` +
        `Your blood group is ${patientData.bloodGroup}. ` +
        `Your phone number is ${patientData.phone}. ` +
        `And your emergency contact is ${patientData.emergencyContact?.name}, ` +
        `who can be reached at ${patientData.emergencyContact?.phone}. ` +
        `This information is kept safe on your device.`

    case 'emergency':
      if (!patientData) return `This is the Emergency SOS page.`
      return `${name}, this is your Emergency SOS page. ` +
        `If you are unwell and need help, show this screen to anyone around you — a doctor, a bystander, or a family member. ` +
        `Your blood group is ${patientData.bloodGroup}. ` +
        `You are allergic to ${patientData.allergies?.join(' and ')}. ` +
        `You have ${patientData.medicalConditions?.join(' and ')}. ` +
        `You are currently taking ${patientData.currentMedications?.join(', ')}. ` +
        `You can also tap the button to show an Emergency QR Code, which gives responders full details quickly. ` +
        `Or call your emergency contact, ${patientData.emergencyContact?.name}, directly from this screen. ` +
        `Stay calm. Help is on the way.`

    case 'shareDoctor': {
      const history = extraData?.sharingHistory || []
      const activeShares = history.filter(h => h.status === 'active')
      const historyLine = activeShares.length > 0
        ? `Right now, you have an active share with ${activeShares.map(h => h.person).join(' and ')}.`
        : `You have no active shares at the moment.`
      return `${name}, this is the Share with Doctor page. ` +
        `When you visit a doctor and they need to see your medical records, just tap Show QR Code. ` +
        `A QR code will appear on your screen — show it to your doctor and they can scan it to see your reports. ` +
        `This share is only valid for 2 hours, so your privacy is protected. ` +
        `You can also stop sharing at any time by tapping Stop Sharing in the history below. ` +
        `${historyLine}`
    }

    case 'shareFamily': {
      const guardians = extraData?.guardians || []
      const guardianLine = guardians.length > 0
        ? `You currently have ${guardians.length} family member${guardians.length > 1 ? 's' : ''} with access — ${guardians.map(g => g.name).join(', ')}.`
        : `You have not added any family guardians yet.`
      return `${name}, this is the Share with Family page. ` +
        `Here you can give a trusted family member — like your son, daughter, or spouse — permanent access to your health records. ` +
        `They will be able to check your reports any time they need to, even if you cannot. ` +
        `To add someone, tap Show QR Code and ask them to scan it on their phone. ` +
        `If you ever want to remove their access, just tap Revoke Access next to their name. ` +
        `${guardianLine}`
    }

    default:
      return `Hello ${name}! How can I help you today?`
  }
}

